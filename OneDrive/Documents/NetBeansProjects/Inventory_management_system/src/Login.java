    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String email = jTextField2.getText();
        String password = jPasswordField2.getText();
        int temp = 0;
        try{
            Connection con = connect.dbConnect();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from user where email = '"+email+"' and password = '"+password+"' and status = 'Active'");
            while(rs.next()){
                temp = 1;
                setVisible(false);
                new Home(rs.getString("userrole")).setVisible(true);
            }
            if(temp == 0){
                JOptionPane.showMessageDialog(null, "Incorrect email or password!");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

